"""
1. append() Method: Functionality: The append() method adds its argument as a single element to the end of the list.
The entire argument is treated as one element, even if it is a list.

Syntax: list.append(object)


2. extend() Method:
Functionality: The extend() method adds each element of its argument to the end of the list. 
If the argument is a list or another iterable (like a string, tuple, etc.), each element from that
iterable is added individually to the list.

Syntax: list.extend(object)

"""